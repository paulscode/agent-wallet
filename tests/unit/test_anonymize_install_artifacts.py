"""Install-time artifact pinning — ``.env.example`` + ``start.sh``.

The Anonymize feature requires a non-trivial deployment posture (Fernet
keys, Tor SOCKS listener layout, optional operator-diversity URLs, etc.).
The install-time artifacts (``.env.example`` for manual deploys,
``start.sh`` for the interactive wizard) MUST stay in sync with
``app/core/config.py``. These tests pin:

* Every Fernet-key setting that the wallet refuses to start without is
  enumerated in ``.env.example`` and auto-generated by ``start.sh``.
* The curated alt-operator onions in ``start.sh`` match the API onions
  that were verified against ``/v2/version`` on the verification date.
  A silent rename or a typo'd hex character would route reverse swaps
  to an unintended host — failing the assertion is preferable to
  failing the deployment at first quote.
* The advisory copy in the wizard names the docs page operators
  can read for the threat model + diligence checklist.
"""

from __future__ import annotations

from pathlib import Path

_REPO = Path(__file__).resolve().parents[2]
_ENV_EXAMPLE = _REPO / ".env.example"
_START_SH = _REPO / "start.sh"


