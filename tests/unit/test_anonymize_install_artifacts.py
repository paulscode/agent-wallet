def test_env_example_lists_required_anonymize_keys() -> None:
    """Every anonymize knob that a production deployment must consider
    appears in .env.example with a comment + default value (or blank)."""
    text = _ENV_EXAMPLE.read_text(encoding="utf-8")
    missing = [k for k in _REQUIRED_ANONYMIZE_KEYS if f"\n{k}=" not in text]
    assert not missing, f"missing keys from .env.example: {missing}"


def test_start_sh_emits_required_anonymize_keys() -> None:
    """start.sh writes every required key into the generated .env. The
    heredoc references the bash variable, so we look for ``KEY=${var}``."""
    text = _START_SH.read_text(encoding="utf-8")
